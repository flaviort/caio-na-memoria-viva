                <h3 class="title-big title">
                    Túlio Simas Piva
                </h3>

                <p class="text-medium">
                    Santiago, RS, 4 de dezembro de 1915 – Porto Alegre, 4 de fevereiro de 1993. Violonista, compositor e poeta. Seu repertório sempre valorizou o choro, o samba e a música popular. Entre seus sambas mais famosos, está Tem que ter mulata, Gente da Noite e Pandeiro de Prata.<br><br>

                    Meu violão<br>
                    Meu violão tem alma, canta e chora<br>
                    Quando sente a carícia dos meus dedos.<br>
                    Percebo estranhas vozes nessa hora,<br>
                    Num murmúrio de sonhos e segredos.<br><br>

                    Meu violão tem alma, canta e chora!<br>
                    E as suas cordas, tensas, retesadas,<br>
                    Plangendo acordes pela noite afora,<br>
                    Pedem luas, estrelas, madrugadas...<br><br>

                    Entre bordões e no roçar de arpejos,<br>
                    Há rumor de suspiros e de beijos<br>
                    E uma imensa tristeza de canção.<br><br>

                    Talvez o meu violão seja morada<br>
                    De alguma solitária alma penada,<br>
                    Confinada na própria solidão.<br><br>

                    Fonte: Ruadospoetas – Antologia 1. Santiago : Pallotti, 2011,p. 64
                </p>

