                    <b>“Estrela cadente é, segundo creio, O flete da Virgem Maria Que disparou com os arreios.”</b><br /><br />

                    Aureliano de Figueiredo Pinto – Tupanciretã, RS, 1º de agosto de 1898 – Santiago, RS, 22 de setembro de 1959. Médico, político ativo, poeta e romancista. Escrevia poemas e artigos literários desde a juventude, porém, a sua modéstia fez com que adotasse um pseudônimo conhecido apenas pelos íntimos: Júlio César de Castro, ou apenas J.C de C. Teve seus primeiros versos publicados no semanário Ox, de Santa Maria, colaborou na revista Kodak, de Porto Alegre, na revista Acadêmica e, com alguma freqüência na Ilustração Pelotense. Publicou seu primeiro livro por insistência dos amigos que selecionaram e organizaram o material a seu pedido. Nobre e humanitário, fez da medicina um sacerdócio. Em quarenta anos de exercício da profissão não acumulou bens.<br /><br />

                    Obras: Romances de Estância e Querência – Marcas do Tempo (Poesia, 1959), Romances de Estância e Querência – Armorial de Estância e Outros Poemas (1963), Memórias do Coronel Falcão (Romance, 1963), Memórias do Coronel Falcão (Romance, 1973), e Itinerário – Poemas de cada Instante (1998).
