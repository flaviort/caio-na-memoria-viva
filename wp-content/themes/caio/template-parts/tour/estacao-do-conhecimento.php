                <h3 class="title-big title">
                    Estação do Conhecimento
                </h3>

                <p class="text-medium">
                    A Estação do Conhecimento foi planejada para valorizar a história e o patrimônio da Terra dos Poetas.<br /><br />

                    É um espaço dinâmico de cultura que busca fortalecer a identidade cultural de Santiago/RS, oportunizando momentos de conhecimento e lazer, bem como evidenciar a importância que a Estação Férrea teve para o desenvolvimento local, tornando viva a memória ferroviária.<br /><br />

                    Seus atrativos:<br /><br />

                    <b>Pavimento Térreo: Memorial Ferroviário</b><br />
                    São quatro salas que contam, através de painéis e mobiliário, a história dos trens e da ferrovia no mundo, Brasil, Rio Grande do Sul e Santiago. Os visitantes conhecem um pouco das ferrovias pioneiras, as primeiras locomotivas e principalmente a relação da cidade com a chegada do trem.<br /><br />

                    <b>Pavimento Superior: Memorial dos Poetas</b><br />
                    É um espaço destinado à memória dos artistas homenageados nas duas primeiras quadras da Rua dos Poetas, onde figuram personalidades como Aureliano de Figueiredo Pinto, Túlio Piva, Caio Fernando Abreu, entre outros. Além de possuir uma sala exclusiva para a história do município, justificando a identidade cultural de Terra dos Poetas.<br /><br />

                    <b>A História:</b><br /><br />

                    A Estação Santiago faz parte da linha Porto Alegre – Uruguaiana dentro do ramal que, partindo de Dilermando de Aguiar faz conexão  com São Borja e Cerro Largo. Essa linha destinava-se a fazer ligação entre a capital com a fronteira oeste, inicialmente, mais por objetivo estratégico do que econômico.<br /><br />

                    Construída pelo 1º Batalhão Ferroviário, foi inaugurada no dia 24 de junho de 1936 e no dia 1º de julho do mesmo ano, a chegada do primeiro trem era comemorada na Vila, enquanto continuavam as obras até São Borja. Vários engenheiros-chefes e empreiteiros fixaram-se na localidade nessa fase, o que contribuiu para um grande movimento social e cultural.<br /><br />

                    Desde o estabelecimento do Exército, até o fim da década de 1930, com a chegada do trem a Santiago, foram inúmeras as transformações ocorridas em todos os aspectos da vida social. Sendo que em 31 de março de 1938 a Vila de Santiago foi elevada à condição de Cidade.<br /><br />

                    <b>O Projeto:</b><br />
                    Com o fim do transporte de passageiros na cidade o patrimônio predial ferroviário ficou em pleno abandono, se deteriorando com a ação do tempo e do vandalismo. E assim foi durante muitos anos, até que a partir de 2009, a Prefeitura Municipal de Santiago iniciou o processo de revitalização dos prédios, investindo na qualificação do espaço e tornando-o um Ponto de Cultura a serviço da comunidade regional.<br /><br />

                    Fez-se uso de recursos próprios e dos profissionais do quadro funcional do município, o que possibilitou que o sonho torna-se realidade. E no dia 03 de janeiro de 2011, nas comemorações de 127 anos de Santiago, foi inaugurado o espaço cultural, que se tornou o principal ponto turístico da Terra dos Poetas, cenário muito utilizado para fotos que percorrem as redes sociais. Atualmente encontra-se fechada para visitação, aguardando uma reforma em sua estrutura.
                </p>

