                <h3 class="title-big title">
                    Fragmento Caio - Estação
                </h3>

                <p class="text-medium">
                    “Suponhamos que eu tivesse levado três, no máximo cinco minutos para apanhar a mochila, essa bagagem típica e mínima de quem não se importa de andar de lá para cá o tempo todo sem paradeiro, saltar do trem e subir as escadas da plataforma até o corredor de saída, naquele passo meio desconfiado dos recém-chegados a algum lugar onde nunca estiveram antes.  Suponhamos também que tenha olhado em volta à procura de K ou de qualquer outra pessoa inteiramente desconhecida e parada na estação deserta, segurando um cartaz com meu nome escrito, e talvez então tivesse me detido um momento a pensar vago que sempre foi um dos meus sonhos – esse: desembarcar numa estação deserta e desconhecida para encontrar alguém igualmente desconhecido segurando meu nome num cartaz erguido bem alto, sobre todas as outras cabaças dos que partem ou chegam, pois essa é a estação que imagino (...)”<br /><br />

                    Caio F. Bem longe de Marienbad. In: Estranhos Estrangeiros. São Paulo : Companhia das Letras, 1996, p. 17 e 18.
                </p>

