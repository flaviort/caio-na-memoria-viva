                <h3 class="title-big title">
                    A Raiz do Pampa
                </h3>

                <p class="text-medium">
                    Há uns vinte dias atrás, me vi diante de uma decisão difícil. Três opções: ir a Montevidéu, para a abertura de uma nova livraria; a Santiago, para inauguração de uma foto minha no Centro Cultural ou baixar hospital imediatamente para extirpar uma vesícula totalmente infeccionada. Pensei: preciso, antes de mais nada, salvar minha vida. E a maldita vesícula era quem vinha causando todos os bodes dos últimos meses. Mas pensei também: ah, Montevidéu. No Uruguai que não conheço mas que tanto amo literariamente. Onetti, Quiroga, Felisberto Ernandes. Poder conhecer pessoalmente o Alfredo Fressia, que fez uma bela matéria comigo para o suplemento literário do jornal El País (sai às sextas e é facílimo de encontrar em Porto). Mas e Santiago? E meu Passo da Guanxuma? Há 12 anos eu não ia lá- desde o centenário da cidade. E antes desses 12 haviam se passado outros 12 sem ir. De repente, pensei: 12, o ciclo completo de Júpiter.<br /><br />
                
                    - Vou a Santiago, disse ao meu médico. Mas, ele tentou dizer. Vou e pronto, expliquei. É um assunto espiritual.<br /><br />

                    Depois disse, meu irmão Felipe, grande e solidário companheiro, aceitou me levar de carro. E fui, e fomos percorrendo o Rio Grande do Sul abaixo. Deus, como é belo o Pampa. Pela janela pouco a pouco foram vindo as paisagens a bico de pena e aquarela. Capões solitários entre colinas suavíssimas, revoadas de garças alvas. Ranchos perdidos, taperas solitárias no descampado. Açudes transparentes refletindo fiados de nuvens do céu absolutamente azul. Gradações de luz e cor, nos verdes, nos claros, nas sombras. Tudo como que pintado e quase sem seres humanos. Alguma chinoca na beira da estrada. Oriental, o Pampa é oriental. Budista. Essencial.<br /><br />

                    E suspirávamos, chegando cada vez mais perto dos paraísos ecológicos de Jaguari e Ernesto Alves. E então, o antigo aeroporto. Os campos onde roubávamos girassol. Eu e o Augusto, que mora na Noruega. Mas cada vez que vem para o Brasil vai direto para lá, para o Pampa...<br /><br />

                    Nos três dias seguintes, minha tia preferida Elcy Abreu Flores fazendo comidinhas deliciosas. Meu primo Airton, lidando com seu som e namoradas gatíssimas. Tanta gente, tanta coisa boa, vital, verdadeira. As mudas de Palmeira e hibisco roxo que pedi ao vizinho. A roseira trepadeira que a tia Elcy me deu. Tanto povo da memória revivida. A lua cheia enorme, dourada, entrando pela janela do quarto onde dormi, para banhar meu pobre corpo escalavrado.<br /><br />

                    Depois, a volta lenta pelo Pampa budista. Eu e meu irmão, querendo mudar para lá.<br /><br />

                    Três dias depois, eu estava no hospital. Catéteres. Dores. Morfina. Médicos delicadíssimos. A cara da morte debruçando-se sobre a minha. Sobrevivi. Estou aqui. "Você é forte", me disseram. De onde vem tanta energia? Veio de Santiago, do meu Passo da Guanxuma. Por que? Porquê, eu não soube responder, mas penso agora: porque é da própria raiz que o ser vivo arranca a sua energia. E a minha raiz está lá, plantada fundo nas aquarelas japonesas de Érico Veríssimo, Cyro Martins, Sérgio Faraco. Não se trata de regionalismo, mas sim de religião, re-ligare. E graças ao meu bom Deus e a todos os anjos- reais ou imaginários que me cercaram por esse dezembro, eu estou vivo. E isso é muito bom.<br /><br />

                    Uma das últimas crônicas escritas por <b>Caio Fernando Abreu</b>, em 1995.
                </p>

