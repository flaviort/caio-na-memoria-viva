											<p class="text-medium">
												O projeto da Rua dos Poetas teve como criador o  prefeito de Santiago/RS, Júlio César Viero Ruivo, e, como arquiteta responsável, Ana Paula Bertani da Silva. Os órgãos responsáveis são a Secretaria de Planejamento (encarregada do projeto), Secretaria de Obras e Viação (encarregada pela execução) e SMEC (encarregada da pesquisa sobre os poetas e artistas homenageados na Rua dos Poetas).<br /><br />

												Sobre os materiais que foram utilizados para a construção dos monumentos da Rua dos Poetas, a arquiteta destaca que foi escolhido um material durável e resistente a intempéries, salientando que o processo de produção e execução desses passou por diversos estudos até ficar na forma atual. A base de concreto dos monumentos, em forma de pena estilizada, foi desenhada pela estagiária de arquitetura, hoje arquiteta, Juliana Kusbick Volpato Vechietti, sob orientação e coordenação de Ana Paula Bertani. Essa estrutura é feita de concreto armado, com placa de bronze e efígie (imagem em relevo). Destaca-se que o símbolo da lira, executado em ferro em cada um dos monumentos, foi criado por Oracy Dornelles, sendo que esse símbolo está vinculado à poesia desde a antiguidade.<br /><br />

												Na Rua utilizou-se por critério uma homenagem póstuma e, dessa forma, a Secretaria Municipal de Educação elegeu e selecionou os artistas a serem homenageados, já falecidos e de notória tradição artística/literária, bem como, suas obras que seriam mencionadas. É relevante destacar, tratando-se do histórico da Rua dos Poetas, que esse projeto teve início em 2001 e foi tornando-se gradativamente real.<br /><br />

												A Rua dos Poetas tem como principal objetivo auxiliar na consolidação do título de “Terra dos Poetas” que Santiago recebeu, através da Lei Municipal 049/98 e Lei Estadual 13.150/09 materializando a arte em suas diversas formas, dentre elas a poesia. Além disso, a rua é um dos focos e está inserida num roteiro cultural e turístico que está sendo estruturado, no qual a base – ponto número 1 – é a Estação do Conhecimento (prédio da antiga estação ferroviária). Fazem parte deste circuito cultural a Estação do Cinema, as Estações do Saber (duas unidades em construção), os Memoriais da Cultura Regionalista e da Cultura Contemporânea (a serem implantados na Rua dos Poetas) e o Memorial do Imigrante (a ser implantado em Ernesto Alves).Todos os pontos desse roteiro terão, em sua essência, a cultura e serão enumerados e representados em um mapa turístico.<br /><br />

												Por fim, quanto à distribuição dos poetas homenageados na Rua dos Poetas, salienta-se que se buscou a proximidade com os locais onde o referido escritor freqüentava, morava ou mostrava sua arte. (...)<br /><br />

												A inauguração da Rua ocorreu em 03 de outubro de 2007,na gestão do prefeito José Francisco Gorski e vice-prefeito Julio César Viero Ruivo.O secretário de planejamento era José Fernando Brum do Nascimento, o secretário de obras, Frederico Brider Peixoto e a secretária de Educação e Cultura, Denise Flório Cardoso.<br /><br />

												Depoimento da Arquiteta Ana Paula Bertani. In: @temporal: a escrita em Santiago. Frederico Westphalen : Ed. URI, 2011, p. 45 e 46.
